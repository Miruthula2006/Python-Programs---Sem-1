text="miruthula"
print("upper=",text.upper())#converts a string into upper case
print("isupper=",text.isupper())#returns true if all characters in the string are upper case

text="MIRUTHULA"
print("lower=",text.lower())#converts a string into lower case
print("islower=",text.islower())#returns true if all characters in the string are lower case

text="mIRUTHULA"
print("swapcase=",text.swapcase())#lower case becomes upper case and vice versa
